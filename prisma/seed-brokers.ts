async function main() {
  console.log(' Starting broker database seed...');
  console.log(` Total brokers to seed: ${brokers.length}`);

  let created = 0;
  let skipped = 0;
  let errors = 0;

  for (const broker of brokers) {
    try {
      // Set isActive to true only for brokers with API integration
      const isActive = broker.integrationStatus === IntegrationStatus.API;
      
      await prisma.broker.upsert({
        where: { name: broker.name },
        update: {
          displayName: broker.displayName,
          country: broker.country,
          region: broker.region,
          integrationStatus: broker.integrationStatus,
          supportedAssets: broker.supportedAssets,
          logoUrl: broker.logoUrl,
          websiteUrl: broker.websiteUrl,
          apiDocumentationUrl: broker.apiDocumentationUrl,
          csvTemplateUrl: broker.csvTemplateUrl,
          description: broker.description,
          priority: broker.priority,
          isActive: isActive,
        },
        create: {
          ...broker,
          isActive: isActive,
        },
      });
      created++;
      if (created % 50 === 0) {
        console.log(` Seeded ${created} brokers...`);
      }
    } catch (error) {
      console.error(` Error seeding broker ${broker.name}:`, error);
      errors++;
    }
  }

  console.log('\n Seed Summary:');
  console.log(` Created/Updated: ${created}`);
  console.log(`  Skipped: ${skipped}`);
  console.log(` Errors: ${errors}`);

  // Stats by integration status
  const stats = await prisma.broker.groupBy({
    by: ['integrationStatus'],
    _count: true,
  });

  console.log('\n Brokers by Integration Status:');
  stats.forEach((stat) => {
    console.log(`  ${stat.integrationStatus}: ${stat._count}`);
  });

  // Stats by region
  const regionStats = await prisma.broker.groupBy({
    by: ['region'],
    _count: true,
    orderBy: {
      _count: {
        region: 'desc',
      },
    },
  });

  console.log('\n Brokers by Region:');
  regionStats.forEach((stat) => {
    console.log(`  ${stat.region || 'Unknown'}: ${stat._count}`);
  });

  console.log('\n Broker database seed completed!');
}

main()
  .catch((e) => {
    console.error(' Seed failed:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
